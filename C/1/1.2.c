// program to add two numbers anddisplay its sum say 5 and 7
#include <stdio.h>

int main(){
    int a = 5, b = 7, sum=0;  // declaration and  initialization of variable. sum always initialize to 0
    sum = a+b;
    printf("\n \t sum = %d \n", sum); // formatting and printing value of sum using %d specifier
return 0;
}
