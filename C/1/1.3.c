// program to multiply two numbers and display its product say 10 and 8
#include <stdio.h>

int main(){
    int a = 10, b = 8, product=0;  // declaration and  initialization of variable. product always initialize to 0
    product = a*b;
    printf("\n \t product = %d \n", product); // formatting and printing value of product using %d specifier
return 0;
}
